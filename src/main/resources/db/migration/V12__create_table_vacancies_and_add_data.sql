CREATE TABLE vacancies
(
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name_vacancy        VARCHAR(255)                            NOT NULL,
    salary              INTEGER,
    status_vacancy      VARCHAR(255),
    close_vacancy       date,
    respons_vacancy     VARCHAR(255),
    consumer            VARCHAR(255),
    candidate_vacancy VARCHAR(255),
    CONSTRAINT pk_vacancies PRIMARY KEY (id)
);

